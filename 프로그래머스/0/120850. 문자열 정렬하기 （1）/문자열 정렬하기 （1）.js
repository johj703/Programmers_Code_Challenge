function solution(my_string) {
    return my_string
        // 정규표현식 '/[0-9]/g'를 사용해서 문자열에서 모든 숫자를 찾기
        // match() 메서드는 정규식과 일치하는 모든 문자를 배열로 반환
        // '[0-9]'는 0부터 9까지의 숫자를 의미, g 플래그는 전역 검색을 의미
        .match(/[0-9]/g)
        // 추출된 숫자 문자열들을 오름차순으로 정렬
        // 기본적으로 sort()는 문자열 기준으로 정렬하므로
        // '(a, b) => a - b' 콜백 함수를 사용해서 숫자 기준으로 정렬
        .sort((a,b) => a - b)
        // 정렬된 문자열 숫자들을 실제 숫자 타입으로 변환
        // map() 메서드를 사용하여 배열의 각 요소를 Number() 함수로 변환
        .map((v) => Number(v));
}