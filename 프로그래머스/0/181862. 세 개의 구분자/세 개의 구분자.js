function solution(myStr) {
    const str = myStr
        .replace(/[abc]/g, " ") 
    // 정규식을 사용해서 모든 'a', 'b', 'c'를 공백으로 변경
    // 정규식 설명:
    // - /.../ : 정규식의 시작과 끝을 표시
    // - [abc] : 문자 클래스로, 'a' 또는 'b' 또는 'c' 중 하나와 일치
    // - g : 전역(global) 플래그로, 문자열 전체에서 모든 일치 항목을 찾음
    // 따라서 문자열 내의 모든 'a', 'b', 'c'가 공백(" ")으로 바뀐다.
        .split(" ") // 공백을 기준으로 문자열을 분리.
        .filter((x) => x !== ""); // 빈 문자열을 제거.
    // 결과 배열이 비어있으면 ["EMPTY"]를 반환하고, 그렇지 않으면 처리된 배열을 반환
    return str.length === 0 ? ["EMPTY"] : str;
}