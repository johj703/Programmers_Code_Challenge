function solution(n) {
    // 합성수의 개수를 저장할 변수를 초기화
    let count = 0;
    
    // 1부터 n까지의 각 숫자에 대해
    // 합성수는 4 이상의 수에서만 나타남.(1,2,3은 합성수가 아님)
    for(let i = 4; i <= n; i++){
        // 2부터 i-1까지의 수로 나누어 약수인지 확인
        for(let j = 2; j < i; j++){
            // i가 j로 나누어 떨어지면 i는 합성수
            if(i % j === 0) {
                // 합성수를 찾았으니 카운트를 증가
                count++;
                // 더 이상 검사할 필요가 없으므로 내부 루프를 종료
                break;
            }
        }
    }
    return count;
}