const input = require("fs")
    .readFileSync("/dev/stdin")
    .toString()
    .split(" ")
    .map((value) => +value);

// 구조분해할당으로 시(H)와 분(M) 추출
const [H, M] = input;

// 알람 시간에서 45분을 뺴는 로직
// 현재 분(M)이 45분보다 작으면 이전 시간에서 빌려와야 함
if(M < 45){
    // 현재 시가 0시(자정)인 경우
    if(H === 0){
        // 전날 23시로 변경하고, 60에서 45분을 뺀 후 현재 분을 더함
        // ex) 0시 30분 -> 23시 45분(60 - 45 + 30 = 45)
        console.log(23, 60 - 45 + M);
    } else {
        // 일반적인 경우: 시간을 1 감소시키고 분을 재계산
        // ex) 10시 30분 -> 9시 45분(60 - 45 + 30 = 45)
        console.log(H - 1, 60 - 45 + M);
    }
} else {
    // 현재 분이 45분 이상인 경우: 같은 시간에서 45분만 빼기
    // ex) 10시 50분 -> 10시 5분(50 - 45 = 5)
    console.log(H, M - 45);
}