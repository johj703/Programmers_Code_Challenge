// ===== 오늘 날짜 출력 =====
/* 
    1. 현재 날짜와 시간 정보를 담은 객체 생성
    new Date(): 코드가 실행되는 시점의 날짜/시간 객체
    ex: 2026년 3월 5일 오후 2시 30분 -> Date 객체
*/
const today = new Date();

/*
    2. 연도(year) 가져오기
    getFullYear(): Date 객체에서 4자리 연도를 반환
    ex: 2026
*/
const year = today.getFullYear();

/*
    3. 월(month) 가져오기 및 형식 맞추기
    getMonth(): 0~11 사이의 값 반환(0=1월, 1=2월, ... , 11=12월)
    +1: 실제 월로 변환(0->1, 1->2, ... , 11->12)
    String(): 숫자를 문자열로 변환 (ex: 3 -> "3")
    padStart(2, '0'): 2자리가 되도록 앞에 0 추가
        - "3" => "03" (한 자리면 앞에 0 추가)
        - "12" => "12" (이미 두 자리면 그대로)
*/
const month = String(today.getMonth() + 1).padStart(2, '0');

/*
    4. 일(day) 가져오기 및 형식 맞추기
    getDate(): 1~31 사이의 날짜 반환
    String(): 숫자를 문자열로 변환(ex: 5 -> "5")
    padStart(2, '0'): 2자리가 되도록 앞에 0 추가
        - "5" => "05" (한 자리면 앞에 0 추가)
        - "15" => "15" (이미 두 자리면 그대로)
*/
const day = String(today.getDate()).padStart(2, '0');

/*
    5. YYYY-MM-DD 형식으로 출력
    템플릿 리터럴(` `)을 사용하여 변수를 문자열에 삽입
    ${변수}: 변수의 값을 문자열에 넣음
    ex: year=2026, month="03", day="05"
        -> "2026-03-05"
*/
console.log(`${year}-${month}-${day}`);
