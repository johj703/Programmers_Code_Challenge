// ===== 입력 처리 =====
// 실행 환경에 따라 입력 파일 경로 설정 (백준: /dev/stdin, 로컬: ./input.txt)
const file = process.platform === "linux" ? "/dev/stdin" : "./input.txt";

// 입력 파일을 읽고 문자열 변환 후, 앞뒤 공백 제거, 공백으로 분리 후 숫자로 변환
const [x, y] = require("fs")
  .readFileSync(file)
  .toString()
  .trim()
  .split(" ")
  .map(Number);

// x: 버스가 한 구간을 이동하는 데 걸리는 시간(분)
// y: 버스가 제2공학관에 도착하기까지 남은 시간(분)

// ===== 민준이가 기다려야 하는 최소 시간 계산 =====
// 버스의 순환 경로: 서울대입구역 <-> 제2공학관 (각 x분 소요)

let waitTime;

if (y < x) {
  /*
        경우1: 버스가 서울대입구역 -> 제2공학관으로 가는 중
        - y분 후: 제2공학관 도착
        - y + x분 후: 서울대입구역 도착(민준이 탑승 가능!)
        예: x = 10, y = 3 -> 3분 후 제2공학관, 13분 후 서울대입구역
     */
  waitTime = y + x;
} else {
  /* 
        경우2: 버스가 제2공학관 -> 서울대입구역으로 오는 중
        - (y - x)분 후: 서울대입구역 도착(민준이 탑승 가능!)
        - 그 후 x분 후: 제2공학관 도착
        - 총 (y - x) + x = y분 후에 제2공학관 도착 ✓
        예: x = 10, y = 12 -> 2분 후 서울대입구역, 12분 후 제2공학관
    */
  waitTime = y - x;
}

// ===== 결과 출력 =====
console.log(waitTime);
