// 1. 표준 입력에서 데이터를 읽어와서 줄 단위로 분리 후 숫자 배열로 변환
// 28명의 학생이 제출한 출석번호들을 배열로 저장
// ex: [1, 2, 3, 4, 5, 6 ,7 ,8, ...](28개의 번호)
let N = require('fs').readFileSync(0).toString().split('\n').map(Number);

// 2. 과제를 제출하지 않은 학생들의 출석 번호를 저장할 배열
let x = [];

// 3. 1번부터 30번까지 모든 출석 번호를 확인
for(let i = 1; i < 31; i++){
    // 현재 번호(i)가 제출한 학생 목록(N)에 포함되어 있으면 건너뛰기
    if(N.includes(i)) continue;
    // 포함되어 있지 않으면(과제 미제출) x 배열에 추가
    x.push(i);
}

// 4. 미제출 학생 번호를 오름차순으로 정렬
x.sort((a, b) => a - b);

// 5. 과제를 제출하지 않은 2명의 출석 번호를 각각 한 줄씩 출력
console.log(`${x[0]}\n${x[1]}`);