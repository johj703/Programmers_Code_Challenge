// ===== 입력 처리 =====
// 실행 환경에 따라 입력 파일 경로 설정 (백준: /dev/stdin, 로컬: ./input.txt)
const file = process.platform === "linux" ? "/dev/stdin" : "./input.txt";
// 입력 파일을 읽고 문자열 변환, 앞뒤 공백 제거 후 숫자로 변환
let N = Number(require("fs").readFileSync(file).toString().trim());

// ===== 세금 계산 =====
// 1번 경우: 전체 상금의 22%를 제세공과금으로 납부
// 수령액 = 상금 × (1 - 0.22) = 상금 × 0.78
const case1 = N * 0.78;

// 2번 경우: 상금의 80%를 필요 경비로 인정
// 과세 대상 = 상금 × 0.20 (나머지 20%)
// 세금 = 과세 대상 × 0.22 = 상금 × 0.20 × 0.22 = 상금 × 0.044
// 수령액 = 상금 - 세금 = 상금 × (1 - 0.44) = 상금 × 0.956
const case2 = N * 0.956;

// ===== 결과 출력 =====
// 1번 경우와 2번 경우를 공백으로 구분하여 출력
console.log(`${case1} ${case2}`);
