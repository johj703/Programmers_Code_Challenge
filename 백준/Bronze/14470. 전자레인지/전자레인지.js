// ===== 입력 처리 =====
// 실행 환경에 따라 입력 파일 경로 설정 (백준: /dev/stdin, 로컬: ./input.txt)
const file = process.platform === "linux" ? "/dev/stdin" : "./input.txt";

// 입력 파일을 읽고 문자열 변환 후, 앞 뒤 공백 제거, 공백으로 분리 후 숫자로 변환
const input = require("fs")
  .readFileSync(file)
  .toString()
  .trim()
  .split("\n")
  .map(Number);

// 입력값을 변수에 할당
const A = input[0]; // 원래 고기의 온도(℃)
const B = input[1]; // 목표 온도(℃)
const C = input[2]; // 얼어있는 고기를 1℃ 데우는 데 걸리는 시간(초)
const D = input[3]; // 얼아있는 고기를 해동하는 데 걸리는 시간(초)
const E = input[4]; // 얼어있지 않은 고기를 1℃ 데우는 데 걸리는 시간(초)

// ===== 총 걸리는 시간 계산 =====
let time = 0; // 총 걸리는 시간 초기화

// 경우1: 고기가 얼어있는 상태에서 시작(A < 0)
if (A < 0) {
  // 1단계: A℃에서 0℃까지 올리기
  // 온도 차이가 -A(음수이므로 절대값)이고, 1℃당 C초 걸림
  // 예: -10℃ -> 0℃는 10℃ 올려야 하므로 10 × C초
  time += -A * C;

  // 2단계: 0℃에서 해동하기
  // 얼어있는 상태에서 얼어있지 않은 상태로 변환하는 데 D초 걸림
  time += D;

  // 3단계: 0℃에서 B℃까지 올리기
  // 온도 차이가 B이고, 1℃당 E초 걸림(이제 얼어있지 않은 상태)
  // 예: 0℃ -> 20℃는 20℃ 올려야 하므로 20 × E초
  time += B * E;
}
// 경우2: 고기가 얼어있지 않은 상태에서 시작(A > 0)
else {
  // A℃에서 B℃까지 바로 올리기
  // 온도 차이가 (B - A)이고, 1℃당 E초 걸림
  // 예: 35℃ -> 92℃는 57℃ 올려야 하므로 57 × E초
  time += (B - A) * E;
}

// ===== 결과 출력 =====
// 총 걸린 시간 출력(초 단위)
console.log(time);
